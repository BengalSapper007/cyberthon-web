import { motion, useScroll, useSpring } from "framer-motion";

export default function ScrollProgress() {
    const { scrollYProgress } = useScroll();
    const scaleX = useSpring(scrollYProgress, {
        stiffness: 100,
        damping: 30,
        restDelta: 0.001
    });

    return (
        <motion.div
            style={{
                scaleX,
                transformOrigin: "left",
            }}
            className="fixed bottom-0 left-0 right-0 h-1.5 bg-gradient-to-r from-cyan-500 via-blue-500 to-purple-600 z-[100] shadow-[0_0_15px_rgba(34,211,238,0.5)]"
        >
            {/* Glow Effect */}
            <div className="absolute inset-0 bg-white/20 blur-[2px]" />
        </motion.div>
    );
}
